/* Utility class to provide output to the console.*/
public class Output {
	
	/*
	 * Sends message out to console using System.out.println()
	 */
	public static void printLine(String message) {
		System.out.println(message);
	}
	
	public static void printDouble(double numberOfHours) {
		System.out.println(numberOfHours);
	}

}

